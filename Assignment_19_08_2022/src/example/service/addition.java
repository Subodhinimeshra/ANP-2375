package example.service;

public class addition {
	                             
	//using overloading method
	
      public int add(int a) {
		a++;
		return a++;
		
	}
      
	public double add(int a,double b) {
		double c=a+b;
		return c;
		
	}
	
	public int add(int a,int b) {
		int c=a+b;
		return c;
	
	}
	
 }
